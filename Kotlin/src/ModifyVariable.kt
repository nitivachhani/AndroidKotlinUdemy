fun main() {
    var count = 1
    println("count: $count")
    
    count += 3    // count = count + 3
    println("count: $count")
    count -= 2    // count = count - 2
    println("count: $count")
    count *= 2     // count = count * 2
    println("count: $count")
    count /= 2      // count = count / 2
    println("count: $count")

}